 private void centrarEnPantalla() {
    // Obtener el tama√±o de la pantalla
        Dimension pantalla = Toolkit.getDefaultToolkit().getScreenSize();

       // Obtener el tama√±o del JFrame
        Dimension ventana = this.getSize();
        // Calcular la posici√≥n para centrar el JFrame
        int posX = (pantalla.width - ventana.width) / 2;
        int posY = (pantalla.height - ventana.height) / 2;

        // Establecer la posici√≥n del JFrame en el centro
        this.setLocation(posX, posY);
    }
