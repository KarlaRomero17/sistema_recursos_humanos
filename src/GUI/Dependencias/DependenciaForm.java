    public void editar() throws Exception{
        try {
            dep.setNombre(this.jTextFieldNombre.getText());
            dep.setId(Integer.parseInt(this.txt_id.getText()));
            String nombre = this.jTextFieldNombre.getText().trim(); 
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El campo Nombre no puede estar vac√≠o");
            } else {
                //int msg_alert = JOptionPane.showConfirmDialog(this, "¬øEst√° seguro de modificar?", "Modificar Dependencia", JOptionPane.YES_NO_OPTION);
                Object[] options = {"Aceptar", "Cancelar"};
                int msg_alert = JOptionPane.showOptionDialog(this, "¬øEst√° seguro de modificar?", "Modificar Dependencia", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
            
                if(msg_alert==0){
                    depController.editarDependencia(dep);
                    JOptionPane.showMessageDialog(rootPane, "Dependencia modificada exitosamente", "Confirmaci√≥n", JOptionPane.INFORMATION_MESSAGE );
                    CrearModelo();
                    limpiarCampos();
                }
                limpiarCampos();     
                nueva_dependencia();
            }  
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
