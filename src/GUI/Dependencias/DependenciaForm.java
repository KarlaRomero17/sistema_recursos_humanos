    public void eliminar(int id){
        try {
            
            int filaSeleccionada = jTableDependencias.getSelectedRow(); // Obtiene la fila seleccionada
            Object estadoActtual=0;
            if (filaSeleccionada != -1) { // Verifica si se ha seleccionado alguna fila
                estadoActtual = jTableDependencias.getValueAt(filaSeleccionada, 2); // Obtiene el valor de la columna 3 (√≠ndice 2)
            }
            //System.out.print(estadoActtual);
            boolean status=false;
            if(estadoActtual=="Inactivo"){
                status=true;
            }
            String msjPregunta = status ? "ACTIVAR" : "DESACTIVAR";
            String msjRespuesta = status ? "activado" : "desactivado";
            dep.setId(Integer.parseInt(this.txt_id.getText()));
            Object[] options = {"Aceptar", "Cancelar"};
            int msg_alert = JOptionPane.showOptionDialog(this, "¬øEst√° seguro de " + msjPregunta + "?", "Eliminar Dependencia", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
            if(msg_alert==0){
                depController.eliminarDependencia(dep);
                JOptionPane.showMessageDialog(rootPane, "Dependencia "+ msjRespuesta +" exitosamente", "Confirmaci√≥n", JOptionPane.INFORMATION_MESSAGE );
                CrearModelo();
                limpiarCampos();
            }
            limpiarCampos();
            nueva_dependencia();
        } catch (Exception e) {
        }
    }

    DefaultTableModel Modelo;
    private void CrearModelo() {
       // String [] col = {"ID", "Nombre" };
        Object[] obj = new Object[6];
        try {
            Modelo = (new DefaultTableModel(null, new String[]{
                "#", "Dependencia", "Estado", "Creado por", "Fecha_registro","Acci√≥n"}) {});
            jTableDependencias.setModel(Modelo);
            
            List ls;
            String estado;
            ls = depController.mostrarDependencias();
            for (int i = 0 ; i < ls.size() ; i++) {
                dep = (Dependencias)ls.get(i);
                obj[0] =dep.getId();
                obj[1]=dep.getNombre(); 
                if(dep.isEstado()){
                    estado="Activo" ;
                }else{
                    estado="Inactivo";
                }
                obj[2]=estado; 
                obj[3]=dep.getCreated_by(); 
                obj[4]=dep.getCreated_at(); 
                jTableDependencias.getColumnModel().getColumn(5).setCellRenderer(new btnEliminarTable());
                jTableDependencias.getColumnModel().getColumn(5).setCellEditor(new ButtonEditor());
 
                jTableDependencias.setRowHeight(20);
   
                Modelo.addRow(obj);    
            }
            ls=depController.mostrarDependencias();
            this.jTableDependencias.setModel(Modelo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    private class ButtonEditor extends DefaultCellEditor {
        private JButton button;
        private int clickedRow;

        public ButtonEditor() {
            super(new JTextField()); 

            button = new JButton(); // Creamos el bot√≥n sin un √≠cono inicialmente
            button.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    int row = jTableDependencias.getSelectedRow(); 
                    if (row != -1) {
                        int id = (int) jTableDependencias.getValueAt(row, 0);
                        eliminar(id);
                        //JOptionPane.showMessageDialog(null, "Button clicked at row " + row + ", ID: " + id);
                    }
                }
            });
            button.setFocusable(false);
        }

        @Override
        public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
            clickedRow = row; 
            Object estadoActual = table.getValueAt(row, 2);
            if ("Activo".equals(estadoActual)) {
                button.setIcon(new ImageIcon("src/Imagenes/activar.png"));
            } else {
                button.setIcon(new ImageIcon("src/Imagenes/desactivar.png"));
            }

            return button;
        }

        @Override
        public Object getCellEditorValue() {
            return null; 
        }
    }
    
